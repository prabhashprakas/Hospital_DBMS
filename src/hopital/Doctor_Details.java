            PreparedStatement pst1=con.prepareStatement("UPDATE doctor SET doc_no = (SELECT doctor from id) WHERE doc_name=? and doc_contact=?");
            pst1.setString(1,doc_name.getText());
            pst1.setString(2,doc_contact_txt.getText());
            pst1.executeQuery();
            ////
            PreparedStatement pst2=con.prepareStatement("UPDATE id SET doctor = doctor+1 ");
            pst2.executeQuery();
            ////
            PreparedStatement pst3=con.prepareStatement("SELECT doc_no FROM doctor WHERE doc_name=? and doc_contact=?");
            pst3.setString(1,doc_name.getText());
            pst3.setString(2,doc_contact_txt.getText());
            ResultSet rs3 = pst3.executeQuery();
            if(rs3.next()){
                counter.setText(rs3.getString(1));
                String sql4 = "Update doctor set doc_id = 'D'||? WHERE doc_name=? and doc_contact=?";
                PreparedStatement pst4=con.prepareStatement(sql4);
                pst4.setString(1,counter.getText());
                pst4.setString(2,doc_name.getText());
                pst4.setString(3,doc_contact_txt.getText());
                pst4.executeQuery();
                
                PreparedStatement pst5=con.prepareStatement("SELECT doc_id FROM doctor WHERE doc_name=? and doc_contact=?");
                pst5.setString(1,doc_name.getText());
                pst5.setString(2,doc_contact_txt.getText());
                ResultSet rs5 = pst5.executeQuery();
                if(rs5.next()){
                    doc_id_txt.setText(rs5.getString(1));
                    //Update_table();
                }
                //JOptionPane.showMessageDialog(null,"Done Everything");
            }
            else{
                JOptionPane.showMessageDialog(null,"Error Occured!");
            }
