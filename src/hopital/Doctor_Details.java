    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int p = JOptionPane.showConfirmDialog(null,"Do you really want to Delete?","Delete",JOptionPane.YES_NO_OPTION);
        if(p == 0)
        {
            try
            {
                Class.forName("oracle.jdbc.OracleDriver");
                Connection con=DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521/XE","system","india");

                PreparedStatement pst=con.prepareStatement("SELECT * FROM doctor WHERE doc_id=?");
                pst.setString(1,doc_id_txt.getText());
                ResultSet rs = pst.executeQuery();
                if(rs.next())
                {
                    PreparedStatement pst1=con.prepareStatement("DELETE FROM doctor WHERE doc_id=?");
                    pst1.setString(1,doc_id_txt.getText());

                    pst1.execute();
                    JOptionPane.showMessageDialog(null,"Deleted");
                    doc_name_combo.setSelectedItem("");
                    //Update_table();
                    Fillcombodoc();
                    
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"No Doctor of such Doctor Id is there");
                    JOptionPane.showMessageDialog(null,"Please check the id and retry");
                }

            }
            catch(ClassNotFoundException | SQLException | HeadlessException e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }//GEN-LAST:event_DeleteActionPerformed

